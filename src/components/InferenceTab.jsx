const FORMAT_META = {
  torchscript: { ext: ".pt", label: "TorchScript", desc: "Portable TorchScript model for PyTorch inference" },
  onnx: { ext: ".onnx", label: "ONNX", desc: "Cross-platform model for ONNX Runtime, TensorRT, OpenVINO" },
  tensorrt: { ext: ".engine", label: "TensorRT", desc: "NVIDIA-optimized engine for maximum GPU inference speed" },
};

function DownloadModelButton({ runId, runName, project, exportFormat }) {
  const [status, setStatus] = useState("idle");
  const [error, setError] = useState("");

  // Reset status when format changes, but only if not actively downloading
  useEffect(() => {
    setStatus((prev) => (prev === "loading" ? prev : "idle"));
    setError("");
  }, [exportFormat]);

  const fmt = FORMAT_META[exportFormat] || FORMAT_META.torchscript;

  async function handleDownload() {
    if (!project) return;
    setStatus("loading");
    setError("");
    try {
      const sshCmd =
        project.connectionType === "remote" ? project.sshCommand : null;
      const taskClassMap = {
        Classification: "ImageClassifier",
        "Multi-Label Classification": "ImageClassifier",
        "Object Detection":
          project.detectionArch === "yolox" ? "YOLOXDetector" : "ObjectDetector",
        "Semantic Segmentation": "SemanticSegmentor",
        "Instance Segmentation": "InstanceSegmentor",
      };
      const taskClass = taskClassMap[project.taskType] || "ImageClassifier";
      await invoke("download_model", {
        projectPath: project.projectPath,
        runId,
        runName: runName || runId,
        taskClass,
        sshCommand: sshCmd,
        exportFormat: exportFormat || "torchscript",
      });
      setStatus("done");
      notify("Model Downloaded", `${fmt.label} model saved to ~/Downloads/${(runName || runId)}${fmt.ext}`);
    } catch (err) {
      setStatus("error");
      setError(String(err));
      notify("Download Failed", String(err));
    }
  }

  const icon =
    status === "loading" ? (
      <div class="download-spinner" />
    ) : status === "done" ? (
      <svg width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2.5" stroke-linecap="round" stroke-linejoin="round">
        <polyline points="20 6 9 17 4 12" />
      </svg>
    ) : (
      <svg width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">
        <path d="M21 15v4a2 2 0 0 1-2 2H5a2 2 0 0 1-2-2v-4" />
        <polyline points="7 10 12 15 17 10" />
        <line x1="12" y1="15" x2="12" y2="3" />
      </svg>
    );

  return (
    <div class="deploy-download-section">
      <div class="deploy-step-header">
        <svg class="deploy-step-icon" width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">
          <path d="M21 15v4a2 2 0 0 1-2 2H5a2 2 0 0 1-2-2v-4" />
          <polyline points="7 10 12 15 17 10" />
          <line x1="12" y1="15" x2="12" y2="3" />
        </svg>
        <div>
          <div class="deploy-step-title">Download {fmt.label} Model</div>
          <div class="deploy-step-desc">
            {fmt.desc}
          </div>
        </div>
      </div>
      <button
        class="deploy-download-btn"
        onClick={handleDownload}
        disabled={status === "loading"}
      >
        {icon}
        {status === "loading"
          ? "Converting & Downloading…"
          : status === "done"
            ? `Downloaded — ~/Downloads/${(runName || runId)}${fmt.ext}`
            : `Download Model (${fmt.ext})`}
      </button>
      {exportFormat === "tensorrt" && (
        <div class="deploy-step-desc" style={{ marginTop: "6px", fontSize: "11px" }}>
          Requires NVIDIA GPU and TensorRT installed on the target machine. The ONNX model is first exported, then converted to a TensorRT engine.
        </div>
      )}
      {error && (
        <div class="deploy-download-error">{error}</div>
      )}
    </div>
  );
}


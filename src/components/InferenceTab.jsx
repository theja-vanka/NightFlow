      lines.push(``);
      lines.push(I(`fig, ax = plt.subplots(1, figsize=(12, 8))`));
      lines.push(I(`ax.imshow(image)`));
      lines.push(I(`colors = plt.cm.Set3(np.linspace(0, 1, max(len(boxes), 1)))`));
      lines.push(I(`for i, (mask, score, label) in enumerate(zip(masks, scores, labels)):`));
      lines.push(I(`    m = mask[0].cpu().numpy() > 0.5`));
